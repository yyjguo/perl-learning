=pod perl数据类型
 perl 弱类型语言，变量不需要指定类型，perl解释器会根据上下文自动选择匹配类型
 三种基本的数据类型： 标量、数组、哈希；
 1 标量 可以是 数字 字符串 浮点数 ，无严格区分。以“$"开头的变量表示标量
 2 数组 以“@”开头，索引从0开始，如 @arr = (1,2,3);
 3 哈希 无序的键值对集合，键作为下标获取值，以‘%'开头。 %h = ('a'=>1,'b'=>2);
 =cut 


